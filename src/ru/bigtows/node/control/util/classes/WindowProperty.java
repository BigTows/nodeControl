package ru.bigtows.node.control.util.classes;

/**
 * Created by BigTows on 20.03.17.
 */
public class WindowProperty {

    private int width;
    private int height;

    public WindowProperty(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
